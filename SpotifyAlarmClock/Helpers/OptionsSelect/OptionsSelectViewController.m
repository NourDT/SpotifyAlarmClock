#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [self.options count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"optionCell" forIndexPath:indexPath];
    
    Option * option = [self.options objectAtIndex:indexPath.row];
    [[cell textLabel] setText:[option label]];
    if(option.selected)
        [cell setAccessoryType:UITableViewCellAccessoryCheckmark];
    else
        [cell setAccessoryType:UITableViewCellAccessoryNone];
    
    return cell;
}

#pragma mark - Table view delegate
- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSInteger row = [indexPath row];
    Option * option = [self.options objectAtIndex:row];
    UITableViewCell *cell = [tableView cellForRowAtIndexPath:indexPath];
    
    //Check or uncheck cell
    if(!option.selected)
        cell.accessoryType = UITableViewCellAccessoryCheckmark;
    else
        cell.accessoryType = UITableViewCellAccessoryNone;
    
    //Switch select value
    option.selected ^= true;
    
    //Unselect cell
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    
    //Update datasource about changed row
    [self.delegate optionValueChanged:option];
}


